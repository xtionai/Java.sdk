package net.xtion.ai.bean;


public class CompetitorProducts {

    private String comProductName;
    private String comUnit;
    private String originName;
    public void setComProductName(String comProductName) {
        this.comProductName = comProductName;
    }
    public String getComProductName() {
        return comProductName;
    }

    public void setComUnit(String comUnit) {
        this.comUnit = comUnit;
    }
    public String getComUnit() {
        return comUnit;
    }

    public void setOriginName(String originName) {
        this.originName = originName;
    }
    public String getOriginName() {
        return originName;
    }

}